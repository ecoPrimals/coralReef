//! Deploy helpers: udev rules, modprobe.d, and `vfio-pci.ids` snippets from glowplug config.

use coral_glowplug::config;
use coral_glowplug::pci_ids;
use coral_glowplug::sysfs;

/// Try each config path until one loads successfully.
pub(crate) fn try_load_config(config_path: Option<String>) -> Result<config::Config, Vec<String>> {
    let paths = if let Some(path) = config_path {
        vec![path]
    } else {
        config::config_search_paths()
    };

    for path in &paths {
        match config::Config::load(path) {
            Ok(cfg) => return Ok(cfg),
            Err(e) => {
                eprintln!("note: skipping {path}: {e}");
            }
        }
    }

    Err(paths)
}

fn load_config(config_path: Option<String>) -> config::Config {
    match try_load_config(config_path) {
        Ok(cfg) => cfg,
        Err(paths) => {
            eprintln!("error: no valid config found. Tried:");
            for path in &paths {
                eprintln!("  - {path}");
            }
            eprintln!(
                "hint: pass --config <path> or create {}",
                config::system_config_path()
            );
            std::process::exit(1);
        }
    }
}

pub(crate) fn deploy_udev(config_path: Option<String>, output: &str, dry_run: bool, group: &str) {
    let cfg = load_config(config_path);

    if cfg.device.is_empty() {
        eprintln!("error: no devices configured in glowplug.toml");
        std::process::exit(1);
    }

    let mut rules = String::new();
    rules.push_str("# Generated by coralctl deploy-udev — do not edit manually.\n");
    rules.push_str("# Re-run: coralctl deploy-udev\n\n");

    rules.push_str("# /dev/vfio/vfio (container fd) — needed by all VFIO users\n");
    rules.push_str(&format!(
        "SUBSYSTEM==\"vfio\", KERNEL==\"vfio\", GROUP=\"{group}\", MODE=\"0660\"\n\n"
    ));

    let mut seen_groups = std::collections::BTreeSet::new();

    for dev in &cfg.device {
        let group_id = sysfs::read_iommu_group(&dev.bdf);
        if group_id == 0 {
            eprintln!(
                "warning: {}: no IOMMU group found (device missing or IOMMU disabled), skipping",
                dev.bdf
            );
            continue;
        }

        if !seen_groups.insert(group_id) {
            continue;
        }

        let (vendor_id, device_id) = sysfs::read_pci_ids(&dev.bdf);
        let chip = sysfs::identify_chip(vendor_id, device_id);
        let name = dev.name.as_deref().unwrap_or(&chip);

        rules.push_str(&format!(
            "# {name} ({}) — IOMMU group {group_id}\n",
            dev.bdf
        ));
        rules.push_str(&format!(
            "SUBSYSTEM==\"vfio\", KERNEL==\"{group_id}\", GROUP=\"{group}\", MODE=\"0660\"\n\n"
        ));
    }

    if seen_groups.is_empty() {
        eprintln!("error: no IOMMU groups resolved — are devices present and IOMMU enabled?");
        std::process::exit(1);
    }

    if dry_run {
        print!("{rules}");
    } else {
        if let Some(parent) = std::path::Path::new(output).parent()
            && !parent.exists()
        {
            eprintln!(
                "error: parent directory {} does not exist",
                parent.display()
            );
            std::process::exit(1);
        }
        match std::fs::write(output, &rules) {
            Ok(()) => {
                eprintln!(
                    "wrote {} rules for {} IOMMU group(s) to {output}",
                    seen_groups.len(),
                    seen_groups.len()
                );
                eprintln!(
                    "reload udev: sudo udevadm control --reload-rules && sudo udevadm trigger"
                );
            }
            Err(e) => {
                eprintln!("error: failed to write {output}: {e}");
                eprintln!("hint: run with sudo or use --dry-run to preview");
                std::process::exit(1);
            }
        }
    }
}

fn boot_personality_is_vfio(s: &str) -> bool {
    s.eq_ignore_ascii_case("vfio")
}

/// Whether this device's PCI ID should appear in kernel `vfio-pci.ids` / modprobe `ids=`.
///
/// `boot_personality = "vfio"` candidates are included unless:
/// - `role = "driver_override"` (ember manages bind via sysfs, not the built-in PCI ID table), or
/// - Tesla K80 GK210 (`10de:102d`) — same rationale as `scripts/boot/coralreef-dual-titanv.conf`.
fn include_in_kernel_vfio_pci_ids(dev: &config::DeviceConfig, vendor: u16, device: u16) -> bool {
    if !boot_personality_is_vfio(&dev.boot_personality) {
        return false;
    }
    if dev.role.as_deref() == Some("driver_override") {
        return false;
    }
    if vendor == pci_ids::NVIDIA_VENDOR_ID && device == pci_ids::TESLA_K80_GK210_DEVICE_ID {
        return false;
    }
    true
}

fn format_vfio_id(vendor: u16, device: u16) -> String {
    format!("{vendor:04x}:{device:04x}")
}

fn collect_kernel_vfio_pci_id_tuples(cfg: &config::Config) -> (Vec<(u16, u16)>, Vec<String>) {
    let mut tuples = Vec::new();
    let mut warnings = Vec::new();

    for dev in &cfg.device {
        let (vendor, device) = sysfs::read_pci_ids(&dev.bdf);
        if vendor == 0 && device == 0 {
            warnings.push(format!(
                "{}: could not read PCI vendor/device (device missing?)",
                dev.bdf
            ));
            continue;
        }
        if !include_in_kernel_vfio_pci_ids(dev, vendor, device) {
            continue;
        }
        tuples.push((vendor, device));
    }

    tuples.sort_by_key(|&(v, d)| (v, d));
    tuples.dedup();
    (tuples, warnings)
}

/// Build comma-separated `vfio-pci.ids` value (no `vfio-pci.ids=` prefix).
fn vfio_pci_ids_kernel_value(tuples: &[(u16, u16)]) -> String {
    tuples
        .iter()
        .map(|&(v, d)| format_vfio_id(v, d))
        .collect::<Vec<_>>()
        .join(",")
}

fn build_modprobe_conf(tuples: &[(u16, u16)]) -> String {
    let mut out = String::new();
    out.push_str("# Generated by coralctl deploy-boot-config — do not edit manually.\n");
    out.push_str("# Re-run: coralctl deploy-boot-config\n\n");

    let mut vendors = std::collections::BTreeSet::new();
    for &(v, _) in tuples {
        vendors.insert(v);
    }

    out.push_str("# Load vfio-pci before proprietary/open stack so PCI IDs are claimed first.\n");
    if vendors.contains(&pci_ids::NVIDIA_VENDOR_ID) {
        out.push_str("softdep nvidia pre: vfio-pci\n");
    }
    if vendors.contains(&pci_ids::AMD_VENDOR_ID) {
        out.push_str("softdep amdgpu pre: vfio-pci\n");
    }

    out.push('\n');

    if tuples.is_empty() {
        out.push_str("# No PCI IDs selected for kernel vfio-pci binding (see glowplug.toml).\n");
        out.push_str("# options vfio-pci disable_idle_d3=1\n");
    } else {
        let ids = vfio_pci_ids_kernel_value(tuples);
        out.push_str("# PCI IDs for vfio-pci (matches vfio-pci.ids kernel param).\n");
        out.push_str(&format!("options vfio-pci ids={ids} disable_idle_d3=1\n"));
    }

    out
}

/// Generate `/etc/modprobe.d`-style config and `vfio-pci.ids` comma list from `glowplug.toml`.
pub(crate) fn deploy_boot_config(
    config_path: Option<String>,
    modprobe_output: &str,
    vfio_ids_output: Option<&str>,
    dry_run: bool,
) {
    let cfg = load_config(config_path);

    if cfg.device.is_empty() {
        eprintln!("error: no devices configured in glowplug.toml");
        std::process::exit(1);
    }

    let (tuples, warnings) = collect_kernel_vfio_pci_id_tuples(&cfg);
    for w in &warnings {
        eprintln!("warning: {w}");
    }

    let modprobe_body = build_modprobe_conf(&tuples);
    let ids_value = vfio_pci_ids_kernel_value(&tuples);

    if tuples.is_empty() {
        eprintln!(
            "note: no vendor:device IDs selected for kernel vfio-pci.ids (all vfio devices excluded or unreadable)"
        );
    }

    if dry_run {
        print!("{modprobe_body}");
        print!("\n--- vfio-pci.ids (comma-separated, for kernelstub / bootloader) ---\n");
        println!("{ids_value}");
        return;
    }

    if let Some(parent) = std::path::Path::new(modprobe_output).parent()
        && !parent.exists()
    {
        eprintln!(
            "error: parent directory {} does not exist",
            parent.display()
        );
        std::process::exit(1);
    }

    match std::fs::write(modprobe_output, &modprobe_body) {
        Ok(()) => eprintln!("wrote modprobe config to {modprobe_output}"),
        Err(e) => {
            eprintln!("error: failed to write {modprobe_output}: {e}");
            eprintln!("hint: run with sudo or use --dry-run to preview");
            std::process::exit(1);
        }
    }

    if let Some(path) = vfio_ids_output {
        if let Some(parent) = std::path::Path::new(path).parent()
            && !parent.exists()
        {
            eprintln!(
                "error: parent directory {} does not exist",
                parent.display()
            );
            std::process::exit(1);
        }
        let line = format!("{ids_value}\n");
        match std::fs::write(path, &line) {
            Ok(()) => eprintln!("wrote vfio-pci.ids value (one line) to {path}"),
            Err(e) => {
                eprintln!("error: failed to write {path}: {e}");
                std::process::exit(1);
            }
        }
    } else {
        eprintln!("vfio-pci.ids value (pass to kernelstub): vfio-pci.ids={ids_value}");
    }
}
