// ---- IAdd3 (VOP2: V_ADD_NC_U32) ----

impl EncodeOp<AmdOpEncoder<'_>> for OpIAdd3 {
    fn encode(&self, _e: &mut AmdOpEncoder<'_>) -> Result<Vec<u32>, CompileError> {
        encode_vop2_from_srcs(
            isa::vop2::V_ADD_NC_U32,
            &self.dst,
            &self.srcs[0],
            &self.srcs[1],
        )
    }
}

